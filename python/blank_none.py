def Blank():
    a = ""
    if a:
        print("Not Blank")
    else:
        print("Blank")

    if a is None:
        print("None")
    else:
        print("Not None")


Blank()


def isNone():
    b = None
    if b:
        print("Not Blank")
    else:
        print("Blank")

    if b is None:
        print("None")
    else:
        print("Not None")


isNone()
